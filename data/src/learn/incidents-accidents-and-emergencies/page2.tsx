const title = `Safety in tunnels`;
const content = `
Before entering a tunnel, remove sunglasses and switch on dipped headlights. Maintain a safe distance from the vehicle in front, even if traffic is congested.

Look out for variable message signs indicating incidents or congestion.

If your vehicle, or the one in front, breaks down or is involved in an incident in a tunnel:

Switch off the engine.

Turn on hazard warning lights.

Call for help from the nearest emergency telephone point immediately.

If your vehicle catches fire while in a tunnel, drive out if possible without causing further danger. If not, stop, turn on hazard warning lights, attempt to extinguish the fire if small, and call for help from the nearest emergency point.

`;

export default {
    title,
    content,
};
