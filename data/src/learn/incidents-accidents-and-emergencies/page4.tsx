const title = `Stopping at an incident`;
const content = `
If you are the first to arrive at an incident or crash, stop and warn other traffic by switching on hazard warning lights. Do not put yourself at risk.

Ensure emergency services are called immediately.

Turn off engines of vehicles involved in the incident.

Move uninjured people away from the scene.

Vehicles carrying dangerous goods display an orange label or hazard warning plate. Report the details when calling emergency services. Reference The Highway Code for different plates.
`;

export default {
    title,
    content,
};
